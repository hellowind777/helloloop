export function resolveDashboardLocale(preferred) {
  const normalized = String(preferred || "").trim();
  if (MESSAGES[normalized]) {
    return normalized;
  }
  if (normalized.toLowerCase().startsWith("zh")) {
    return "zh-CN";
  }
  return "en-US";
}

export function createTranslator(locale) {
  const resolvedLocale = resolveDashboardLocale(locale);
  const dictionary = MESSAGES[resolvedLocale] || MESSAGES["en-US"];
  const fallback = MESSAGES["zh-CN"];

  return (key, params = {}) => {
    const template = dictionary[key] || fallback[key] || key;
    return template.replace(/%\{([^}]+)\}/gu, (_, name) => String(params[name] ?? ""));
  };
}
